public class AuditWorkOvertimeapproval {

	private String id;
	// 加班单号
	private String code;
	// 申请人
	private String userName;
	// 开始时间
	private String workOvertimeStartTime;
	// 结束时间
	private String workOvertimeEndTime;
	// 加班类型
	private String workOvertimeType;
	// 总时长
	private String workOvertimeTotalTime;
	// 加班的申请时间
	private String workOvertimeapprovetime;
	// 加班事由
	private String workOvertimeReason;
	// 审批状态
	private String workOvertimeStatus;
    /**
     * 加班实体
     * @param id
     * @param code  
     * @param userName
     * @param workOvertimeStartTime
     * @param workOvertimeEndTime
     * @param workOvertimeType 加班类型，事假等
     * @param workOvertimeTotalTime
     * @param workOvertimeapprovetime
     * @param workOvertimeReason
     * @param workOvertimeStatus 
     * @param workOvertimeAudited
     */
	public AuditWorkOvertimeapproval(String id, String code, String userName,
			String workOvertimeStartTime, String workOvertimeEndTime,
			String workOvertimeType, String workOvertimeTotalTime,
			String workOvertimeapprovetime, String workOvertimeReason,
			String workOvertimeStatus) {
		super();
		this.id = id;
		this.code = code;
		this.userName = userName;
		this.workOvertimeStartTime = workOvertimeStartTime;
		this.workOvertimeEndTime = workOvertimeEndTime;
		this.workOvertimeType = workOvertimeType;
		this.workOvertimeTotalTime = workOvertimeTotalTime;
		this.workOvertimeapprovetime = workOvertimeapprovetime;
		this.workOvertimeReason = workOvertimeReason;
		this.workOvertimeStatus = workOvertimeStatus;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getworkOvertimeStartTime() {
		return workOvertimeStartTime;
	}

	public void setworkOvertimeStartTime(String workOvertimeStartTime) {
		this.workOvertimeStartTime = workOvertimeStartTime;
	}

	public String getworkOvertimeEndTime() {
		return workOvertimeEndTime;
	}

	public void setworkOvertimeEndTime(String workOvertimeEndTime) {
		this.workOvertimeEndTime = workOvertimeEndTime;
	}

	public String getworkOvertimeType() {
		return workOvertimeType;
	}

	public void setworkOvertimeType(String workOvertimeType) {
		this.workOvertimeType = workOvertimeType;
	}

	public String getworkOvertimeTotalTime() {
		return workOvertimeTotalTime;
	}

	public void setworkOvertimeTotalTime(String workOvertimeTotalTime) {
		this.workOvertimeTotalTime = workOvertimeTotalTime;
	}

	public String getworkOvertimeapprovetime() {
		return workOvertimeapprovetime;
	}

	public void setworkOvertimeapprovetime(String workOvertimeapprovetime) {
		this.workOvertimeapprovetime = workOvertimeapprovetime;
	}

	public String getworkOvertimeReason() {
		return workOvertimeReason;
	}

	public void setworkOvertimeReason(String workOvertimeReason) {
		this.workOvertimeReason = workOvertimeReason;
	}

	public String getworkOvertimeStatus() {
		return workOvertimeStatus;
	}

	public void setworkOvertimeStatus(String workOvertimeStatus) {
		this.workOvertimeStatus = workOvertimeStatus;
	}

}
